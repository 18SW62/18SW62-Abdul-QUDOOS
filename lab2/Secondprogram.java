class SecondProgram{
	public static void main(String[] args) {
		int x=4,y=5;
		System.out.println("x==y=  "+(x==y));//This will give false in the output because x is not equal to y;
		System.out.println("x>y=   "+(x>y));//This will give false in the output because x is not greater than y;
		System.out.println("x<y=   "+(x<y));//This will give true in the output because x is less than than y;
		System.out.println("x<=y=  "+(x<=y));//This will give true in the output because x is less than than y;
		System.out.println("x>=y=  "+(x>=y));//This will give false in the output because x is not greater than y;
		System.out.println("x!=y=  "+(x!=y));//This will give true in the output because x is not equal to y;
	}
}